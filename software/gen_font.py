# ---------------------------------------------------------------------------
# Pad to full 4096 bytes (256 chars × 16 bytes)
# Characters 0x80-0xFF get blank/block glyphs for now
# ---------------------------------------------------------------------------
def build_full_font(base: bytes) -> bytes:
    """Extend base font data to full 4096 bytes."""
    result = bytearray(base)

    if len(result) > 4096:
        raise ValueError(f"Font data too large: {len(result)} bytes (max 4096)")

    # Fill remaining chars with a simple block glyph so they're visible
    block_glyph = bytes([
        0x00, 0x00, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
        0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00,
    ])

    while len(result) < 4096:
        remaining = 4096 - len(result)
        result.extend(block_glyph[:min(16, remaining)])

    return bytes(result[:4096])


def generate_mem_file(out_path: str) -> None:
    """Write hex mem file for $readmemh."""
    font = build_full_font(FONT_DATA)

    with open(out_path, 'w') as f:
        for byte in font:
            f.write(f"{byte:02x}\n")

    print(f"Written {len(font)} bytes → {out_path}")
    print(f"  {len(font) // 16} characters × 16 rows")

    # Spot-check
    a_offset = 0x41 * 16
    print(f"  'A' (0x41) rows: {' '.join(f'{font[a_offset+i]:08b}' for i in range(4))}...")


